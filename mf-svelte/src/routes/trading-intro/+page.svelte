  <h2>How does trading work</h2>
  <p>
    At its core, trading is simply the act of exchanging goods, services, or assets. In financial markets, this means buying things like stocks, cryptocurrencies, or commodities with the goal of selling what you bought later when the price is higher making a profit from the price rising.<br><br>
    Trading in financial markets works on the same principle as the beyblade analogy – it's all about supply and demand. When demand for an asset goes up and the supply remains constant (or shrinks), its price will rise. When demand falls or supply increases, the price typically drops.<br><br>
    Every asset, whether it's a share of a company's stock, Gold, or a barrel of oil, has fluctuating demand. Traders analyze various factors to predict when demand for a particular asset is likely to increase or decrease. These factors can include:<br>
    • A liquidity gap (a FVG) in stock bar chart price action<br>
    • Economic news (e.g., Are major economies having good GDP at the given time, interest rates of major lending like the US or Japan)<br>
    • Global events (e.g., geopolitical tensions, Bank runs, Transnational)<br>
    • Technological advancements or disruption of world changing technology<br>
    • Market sentiment and quant/investor psychology about the market at a given time<br><br>
    For example, if a company announces a groundbreaking new product, interest/demand for its stock will likely surge resulting in the price of the company stock rising. If only a limited number of shares are available, those shares become even more valuable. For example, that is why Bitcoin's price seems to be always rising, because there is a finite amount of outstanding shares/Bitcoins and none more can be issued, when more demand for Bitcoin comes at a point in time, the price rises with the new demand.
  </p>

  <h3>Stocks</h3>
  <p>
    Stocks represent ownership of a company. This is kind of a weird concept to me still lowkey, but whenever you purchase shares, you own a piece of that business. The Stock price of a company is directly tied to it's ability to remain profitable and the demand of the company's services/products. Traders usually try to figure out if they can profit from a company's stock they believe to be undervalued by looking at price movements driven by earnings reports, market sentiment, macroeconomic factors, along with technical analysis.
  </p>
