  <h3>Futures</h3>
  <p>
    Futures is basically the same thing as a parlay for assets. When you buy an asset like a stock, a commodity like gold, or even a cryptocurrency like Bitcoin in order to lose all you money the price has to go to zero. This is likely never to happen. Vice Versa, it might take a really long time to double your money with a asset you have invested in so people use something called futures to speed up the process. Instead of waiting 2 years to get double your money, a person can buy the "futures" of an asset to speed up their profit time.
  </p>
  <div class="pros-cons">
    <div>
      <h4>Pros:</h4>
      <ul>
        <li>High leverage allows for massive gains from small price movements</li>
        <li>Trading with leverage is often more liquid than holding underlying assets</li>
        <li>Make money faster</li>
      </ul>
    </div>
    <div>
      <h4 class="cons">Cons:</h4>
      <ul>
        <li>High leverage also means magnified losses. You have to know what your doing to trade with leverage</li>
        <li>Requires much better risk management and psychology</li>
      </ul>
    </div>
  </div>

  <h3>Forex (Foreign Exchange)</h3>
  <p>
    The global exchange of currencies. This is usually the most liquid market in the world, also being open 24/7. Currencies fluctuate all the time and you can profit from these fluctuations by trading them. Forex is the largest financial market in the world, with a daily trading volume exceeding $6 trillion.
  </p>
  <div class="pros-cons">
    <div>
      <h4>Pros:</h4>
